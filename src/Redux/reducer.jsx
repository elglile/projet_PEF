export default function Redd( state = Etat,action) {
    switch (action.type) {
        case 'ADD_POST':
            return { ...state, Post: [...state.Post, action.payload] };
        case 'DELETE_POST':
            return {
                ...state,
                Post: state.Post.filter(el => el.id !== action.payload.id)
            };
        case 'UPDATE_POST':
            let updatedPostIndex = state.Post.findIndex(el => el.id === action.payload.id);
            if (updatedPostIndex !== -1) {
                let updatedPosts = [...state.Post];
                updatedPosts[updatedPostIndex] = action.payload;
                return { ...state, Post: updatedPosts };
            }
            return state;
        case 'ADD_TO_LIST':
            let sameGenre = state.Choose.find(el=>el.typeOfService===action.payload.typeOfService)
            if(!sameGenre){
                return{...state, Choose: [...state.Post,action.payload]}
            }else{
                return{...state, Choose: [...state.Post.filter(el=>el.typeOfService!==action.payload.typeOfService),action.payload]}
            }
            return state;
        case 'DELETE_FROM_LIST':
            return {
                ...state,
                Choose: state.Choose.filter(el => el.id !== action.payload.id)
            };            
            return state;
        case 'SEARCH':
            // Similar implementation as UPDATE_POST and DELETE_POST
            return state;
        case 'ADD_PROFIL':
            // Similar implementation as UPDATE_POST and DELETE_POST
            return state;
        case 'UPDATE_PROFIL':
            // Similar implementation as UPDATE_POST and DELETE_POST
            return state;
        case 'DELETE_PROFIL':
            // Similar implementation as UPDATE_POST and DELETE_POST
            return state;
        case 'ADD_COMMENT':
            // Similar implementation as UPDATE_POST and DELETE_POST
            return state;
        case 'DELETE_COMMENT':
            // Similar implementation as UPDATE_POST and DELETE_POST
            return state;
        default:
            return state;
    }
}

















































